package net.praqma.jenkins.pac.command;

import hudson.model.Descriptor;
import hudson.util.FormValidation;
import net.sf.json.JSONObject;
import org.kohsuke.stapler.QueryParameter;
import org.kohsuke.stapler.StaplerRequest;

/**
 *
 * @author Praqma
 */
public abstract class PACRunCommandDescriptor<T extends PACRunCommand> extends Descriptor<PACRunCommand> {

    public PACRunCommand newInstance(StaplerRequest req, JSONObject formData, PACRunCommand instance) throws FormException {
        return super.newInstance(req, formData);
    }

    public FormValidation doCheckTail(@QueryParameter String tail) {
        return FormValidation.validateRequired(tail);
    }
}
